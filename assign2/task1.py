# Two Numbers: Even or Odd

num1 = int(input(" Enter a number: "))
num2 = int(input(" Enter a number: "))

if num1 % 2 == 0:
    print(num1, " is an even number. ")
else:
    print(num1, " is an odd number. ")

if num2 % 2 == 0:
    print(num2, " is an even number. ")
else:
    print(num2, " is an odd number. ")


